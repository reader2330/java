public class Meses2{

	public static void main(String args[]){

		int mes=2;
         
          switch(mes){

          	case 1:

          	  System.out.println("El"+mes+ "es Enero");
          	  break;

          	case 2:

          	  System.out.println("El"+mes+"es Febrero");
          	  break;


          	default:

          	     System.out.println("Numero de mes no valido");

          }
	}
}    